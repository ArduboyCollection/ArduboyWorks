static int counter;

void initLogo()
{
    counter = 120; // 2 secs
}

bool updateLogo()
{
    counter--;
    return (counter <= 0);
}

void drawLogo()
{
    if (counter == 119) {
        arduboy.clear();
        arduboy.drawBitmap(28, 12, imgObnLogo, 72, 40, WHITE);
        arduboy.setCursor(16, 56);
        arduboy.print(F(APP_INFO));
    }
}
